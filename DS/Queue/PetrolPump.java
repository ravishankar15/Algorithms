//Utility class for the circulat tour problem
class PetrolPump{
	int petrol;
	int distance;

	public PetrolPump(int petrol, int distance){
		this.petrol = petrol;
		this.distance = distance;
	}


	public int getPetrol(){
		return this.petrol;
	}

	public int getDistance(){
		return this.distance;
	}
}